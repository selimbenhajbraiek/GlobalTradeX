import { writeFileSync, mkdirSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { geoPath, geoEquirectangular } from "d3-geo";
import { feature } from "topojson-client";
import landTopo from "world-atlas/land-110m.json" with { type: "json" };

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const width = 1000;
const height = 500;

const projection = geoEquirectangular().fitSize([width, height], { type: "Sphere" });
const pathGen = geoPath(projection);
const landFeature = feature(landTopo, landTopo.objects.land);

const landPath = pathGen(landFeature);

const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${width} ${height}" fill="none">
  <rect width="${width}" height="${height}" fill="#E8EBF0"/>
  <path d="${landPath.replace(/"/g, "&quot;")}" fill="#FFFFFF" stroke="#D5D1C9" stroke-width="0.6" stroke-linejoin="round"/>
</svg>`;

mkdirSync(join(root, "public", "maps"), { recursive: true });
writeFileSync(join(root, "public", "maps", "world-base.svg"), svg);

const js = `/** Auto-generated by scripts/generate-world-map.mjs — do not edit by hand */
export const WORLD_MAP_VIEWBOX = { width: ${width}, height: ${height} };
export const WORLD_LAND_PATH = ${JSON.stringify(landPath)};
`;

writeFileSync(join(root, "lib", "world-map-paths.js"), js);
console.log("Wrote public/maps/world-base.svg and lib/world-map-paths.js");
